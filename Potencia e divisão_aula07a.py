n1 = int(input('Digite um Valor: '))
n2 = int(input('Digite outro Valor: '))
soma = n1 + n2
m = n1 * n2
d = n1 / n2
di = n2 // n2
e = n1 ** n2 #Potencia
print('A soma é {}, o produto é {} e a divisão {:.2f}'.format(soma, m, d), end=' ')
print('Divisão Inteira {} e a potência é {}'.format(di, e))














